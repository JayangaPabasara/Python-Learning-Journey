# Age Calculator

birth_year = input("Enter Your Birth Year: ")

birth_year = int(birth_year)
age_when_2024 = 2024 - birth_year
_30th_year = birth_year + 30

print('======= Result ==========')
print(f'When 2024 you are {age_when_2024} years old.')
print(f'In {_30th_year} year you are 30 years old')
